package com.ucpb.tfs.swift.message;

/**
 */
public class MIR {

    private String inputDate;
    private String swiftAddress;
    private String inputSessionNumber;
    private String isn;

    public String getInputDate() {
        return inputDate;
    }

    public void setInputDate(String inputDate) {
        this.inputDate = inputDate;
    }

    public String getSwiftAddress() {
        return swiftAddress;
    }

    public void setSwiftAddress(String swiftAddress) {
        this.swiftAddress = swiftAddress;
    }

    public String getInputSessionNumber() {
        return inputSessionNumber;
    }

    public void setInputSessionNumber(String inputSessionNumber) {
        this.inputSessionNumber = inputSessionNumber;
    }

    public String getIsn() {
        return isn;
    }

    public void setIsn(String isn) {
        this.isn = isn;
    }
}
